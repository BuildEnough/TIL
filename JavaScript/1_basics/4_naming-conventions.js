/**
 * Naming Convention 2
 * 
 * 1) camelCase: 첫 번째 글자는 소문자, 띄어쓰기 대신 다음 글자를 대문자로 사용
 * 2) snake_case: 소문자만 사용, 띄어쓰기 대신 _를 사용
 * 3) PascalCase: 첫 번째 글자도 대문자로 사용, 띄어쓰기 대신 다음 글자를 대문자로 사용
 */

const anYuJin = '안유진';
console.log(anYuJin);