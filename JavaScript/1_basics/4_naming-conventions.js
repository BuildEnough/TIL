/**
 * Naming Convention
 * 
 * 1) 웬만하면 영어 사용
 * 2) 특수기호 _와 $만 사용가능
 * 3) 숫자로 이름을 시작할 수 없다 ex. 1Name, 2Hello
 * 4) 키워드는 변수명으로 사용할 수 없다 ex. var, let, const, ...
 */

var $live = '아이브';
const _yuJin = '안유진';
let enough = 'ENOUGH';

console.log($live);
console.log(_yuJin);
console.log(enough);


